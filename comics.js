  window.onload = buildList;

  function buildList() {
    const url = new URL(window.location.href);
    const params = url.searchParams;
    const pageName = params.get('page');

    const select = document.getElementById('select-comic');
    comics.forEach((comic, i) => {
      comic.files.forEach((file, j) => {
        const element = document.createElement('option');
        const value = `${i}-${j}`;
        element.id = value;
        element.value = value;
        element.text = `${comic.arc} - ${j + 1}`;
        if (pageName === value) {
          element.selected = true;
        }
        select.appendChild(element);
      });
    });

    const img = document.getElementById('comic-image');

    const prev = document.getElementById('prev');
    const next = document.getElementById('next');
    const last = document.getElementById('last');
    let prevChapter = document.getElementById('prev-arc');
    let nextChapter = document.getElementById('next-arc');

    const prevUrl = new URL(window.location.href);
    const nextUrl = new URL(window.location.href);
    const lastUrl = new URL(window.location.href);
    const prevChapterUrl = new URL(window.location.href);
    const nextChapterUrl = new URL(window.location.href);

    const lastArc = comics[comics.length - 1];
    lastUrl.searchParams.set(
      'page',
      `${comics.length - 1}-${lastArc.files.length - 1}`
    );
    last.href = lastUrl.toString();

    if (!pageName) {
      img.src = `img/${comics[0].files[0]}`;
      nextUrl.searchParams.set('page', '0-1');
    } else {
      const [arcIndex, fileIndex] = pageName
        .split('-')
        .map((number) => parseInt(number));
      const fileName = comics[arcIndex].files[fileIndex];
      img.src = `img/${fileName}`;

      if (comics[arcIndex].files[fileIndex + 1]) {
        nextUrl.searchParams.set('page', `${arcIndex}-${fileIndex + 1}`);
      } else if (comics[arcIndex + 1]) {
        nextUrl.searchParams.set('page', `${arcIndex + 1}-0`);
      }

      if (fileIndex > 0) {
        prevUrl.searchParams.set('page', `${arcIndex}-${fileIndex - 1}`);
      } else if (fileIndex === 0 && arcIndex > 0) {
        const prevArc = comics[arcIndex - 1];
        prevUrl.searchParams.set(
          'page',
          `${arcIndex - 1}-${prevArc.files.length - 1}`
        );
      }

      if (comics[arcIndex + 1]) {
        nextChapterUrl.searchParams.set('page', `${arcIndex + 1}-0`);
      }

      if (arcIndex > 0) {
        prevChapterUrl.searchParams.set('page', `${arcIndex - 1}-0`);
      }
    }

    prev.setAttribute('href', prevUrl.toString());
    next.setAttribute('href', nextUrl.toString());
    nextChapter.setAttribute('href', nextChapterUrl.toString());
    prevChapter.setAttribute('href', prevChapterUrl.toString());

    select.addEventListener('change', function (event) {
      if (event.target.value === 'default') {
        return;
      }
      params.set('page', event.target.value);
      window.location.search = params;
    });
  }
